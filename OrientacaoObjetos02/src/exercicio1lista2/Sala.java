package exercicio1lista2;

public class Sala {
	private int numSala;
	private int capacidade;
	
	public Sala(int numSala, int capacidade) {
		
		this.numSala = numSala;
		this.capacidade = capacidade;
	}

	public int getNumSala() {
		return numSala;
	}

	public int getCapacidade() {
		return capacidade;
	}
	
	 
}
